#Solicita ao usúario um número
numero = float(input('Digite um numero:'))

#Calcula os resultados
dobro = 2 * numero
triplo = 3 * numero
raiz = numero ** 0.5

#Exibe os resultados
print(f'O numero analisado foi: {numero}')
print(f'O dobro desse numero é: {dobro}')
print(f'O triplo desse numero é: {triplo}')
print(f'A raiz quadrada desse numero é: {raiz}')
